def main() -> None:
    slides = load_slide_content()
    style = read(OUTPUT / "00_visual_style_brief.md")
    project = project_name(materials_text())
    IMAGE_PROMPTS.mkdir(parents=True, exist_ok=True)
    for old_prompt in IMAGE_PROMPTS.glob("slide_*.txt"):
        old_prompt.unlink()
    all_prompts = ["# 每页生图提示词", "", "> 提示词结构采用：全局总控 + 页面模板 + 单页内容 + 额外锁定 + 自检清单。", ""]
    for slide in slides:
        prompt = prompt_for(slide, style, project)
        name = IMAGE_PROMPTS / f"slide_{slide['slide_no']:02d}.txt"
        write(name, prompt)
        all_prompts.append(f"## slide_{slide['slide_no']:02d}.txt")
        all_prompts.append("")
        all_prompts.append("```text")
        all_prompts.append(prompt.strip())
        all_prompts.append("```")
        all_prompts.append("")
    write(OUTPUT / "03_image_prompts.md", "\n".join(all_prompts))
    write(OUTPUT / "04_generation_guide.md", """# 图片生成指南

1. 每页提示词均采用“全局总控 + 页面模板 + 单页内容 + 额外锁定 + 自检清单”的完整结构。
2. 如果用户没有上传图片或自定义风格，直接采用“国企汇报默认风格”。
3. 先生成前 3-5 页小样，不要直接生成全量。
4. 小样范围通常为封面、目录、背景、问题、目标/方案。
5. 用户确认小样后，再生成全量图片。
6. 如页面需要真实 Logo 或产品截图，优先生成不含真实素材的底图，再后期叠加官方素材。
7. 不得让图片模型凭空生成近似 Logo、伪造产品界面或无来源数据。
""")
    print(f"Wrote {len(slides)} rich image prompts.")


if __name__ == "__main__":
    main()
