def clean_text(value: str, limit: int = 120) -> str:
    value = re.sub(r"\s+", " ", str(value or "")).strip()
    return value[:limit]


def numbered_items(items: list[str], fallback: list[str], limit: int = 6) -> list[str]:
    merged: list[str] = []
    for item in items + fallback:
        item = clean_text(item, 80).strip("。；; ")
        if item and item not in merged:
            merged.append(item)
        if len(merged) >= limit:
            break
    return merged


